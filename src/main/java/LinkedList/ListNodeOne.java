/*
    Associated with ReverseLinkedList

    Definition for singly-linked list
 */

package LinkedList;

public class ListNodeOne {
    int val;

    ListNodeOne next;

    ListNodeOne() {
    }

    ListNodeOne(int val) {
        this.val = val;
    }

    ListNodeOne(int val, ListNodeOne next) {
        this.val = val;
        this.next = next;
    }

}
