package Trees;

public class TreeNodeThree {
    int val;
    TreeNodeThree left;
    TreeNodeThree right;
    TreeNodeThree() {}
    TreeNodeThree(int val) { this.val = val; }
    TreeNodeThree(int val, TreeNodeThree left, TreeNodeThree right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
}
